                <p>
                    <span style="font-size: small">e-Krishok is a campaign designed to bring the 
                    benefits of ICT to the farmers of rural Bangladesh.<span 
                        style="mso-spacerun:yes">&nbsp; </span>ICT can be used to deliver 
                    information and advisory services to farmers in rural and remote locations at a 
                    very low cost, and with this information and advisory service farmers can derive 
                    benefit which that farmer would otherwise not have been able to access.<span 
                        style="mso-spacerun:yes">&nbsp; </span>With access to such information and 
                    advisory services, farmers are able to improve their livelihoods, income; and 
                    hence plan for the future in a more secure frame of mind.Further information 
                    about e-Krishok can be found at <a href="http://www.ekrishok.com"> www.ekrishok.com</a>.<o:p></o:p></span></p>
                <p>
                    <span style="font-size: small">The e-Krishok campaign has utilized the 
                    infrastructure of existing tele-centers to deliver such information and advisory 
                    services.Whenever a farmer has an issue, question or problem related to agriculture, 
                    livestock, poultry or any other income generating activity, that farmer can 
                    visit the tele-center and receive the correct information in a timely manner.<o:p></o:p></span></p>
