                        Dr. 
                        M. Asaduzzaman joined the Pakistan Institute of Development 
                        Economics, the precursor of the BIDS, in 1969 as Staff 
                        Economist. He holds his current post since end 1997. Key 
                        capabilities of Dr. M. Asaduzzaman encompass several 
                        areas of professional and institutional activities. He 
                        has been educated in the London School of Economics and 
                        the University of Sussex, England. He has a research 
                        experience in various fields. Generally they fall in the 
                        area of agriculture, natural resource management and 
                        rural development. Within this broad area, his past 
                        research includes one of the first rigorous studies on 
                        technological change in agriculture (Ph.D. thesis), 
                        institutional and management issues in rural 
                        infrastructure development (one of the first rigorous 
                        such analysis of WFP-aided programs anywhere in the 
                        world), environmental issues such as coastal 
                        environmental management, climate change, comprehensive 
                        evaluation of water resource development, comparative 
                        evaluation of poverty eradication programs under 
                        alternative institutional frameworks, critical review of 
                        long-term historical record of growth performance of 
                        Bangladesh agriculture, energy modeling, particularly 
                        related to climate change issues, local level planning, 
                        local government resource mobilization, construction of 
                        I-O model for Bangladesh and agriculture trade and 
                        globalization and modeling of free trade arrangements.
                        <br>
                        <br>
                        Dr. Asaduzzaman&#8217;s current research interests include 
                        modeling of Bangladesh trade under WTO and free trade 
                        rules, rural energy, energy development and its 
                        governance with particular reference to the power 
                        sector, and private investment in schooling. <br>

                        <br>
