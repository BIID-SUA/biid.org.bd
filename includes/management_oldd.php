Md Shahid Uddin Akbar is the Chief Executive Officer 
                        (CEO) at Bangladesh Institute of ICT in Development (BIID), 
                        a private sector initiative to promote the use of ICT as 
                        a tool for development. BIID covers research and 
                        development on telecentres, e-Governance, capacity 
                        building and content development. <br>
                        <br>

                        Md Shahid Uddin Akbar, born on August 4, 1970 at 
                        Chittagong, Bangladesh completed his Masters in Finance 
                        and Banking, Mr. Akbar joined a private commercial bank 
                        and served as Credit Officer for 8 eight years.<br>
                        <br>
                        During working in Bank, Mr. Akbar volunteered in various 
                        ICT4D initiatives. In 2000, Mr. Akbar established a 
                        voluntary organization titled ICT for Development 
                        Program Bangladesh (ICTDP&#8217;B) to bring the benefits of 
                        ICT in the rural areas. He contributed in the World 
                        Summit on the Information Society (WSIS) at national and 
                        international level.<br>
                        <br>
                        Mr. Akbar worked as ICT Consultant in SwissContact/Katalyst, 
                        Bangladesh from July 2004 to June 2008. Katalyst, a 
                        multi donor funded project promoting competitiveness of 
                        small and medium enterprises and addressing the &#8216;information failure&#8217; of rural areas. Katalyst is 
                        implementing ICT4D projects to bridge the gap with 
                        special focus to farmer, rural business and livelihood 
                        as a whole. Under this project, new business model for 
                        establishing Telecenter (Rural ICT Center) in rural 
                        areas has been developed to serve the rural communities 
                        in a sustainable and commercially viable model. <br>

                        <br>
                        Mr. Akbar is also serving as a Member of the Executive 
                        Committee of Bangladesh Telecentre Network (BTN), a 
                        coalition of telecentre practitioners in Bangladesh. He 
                        also involved with Junior Chamber International, 
                        Internet Society and Diplo Foundation activities.</td>
