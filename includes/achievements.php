<ul style="list-style-type:decimal;margin-left:-20px">
  <li>In July 2008, BIID launched as a private enterprise supported by Katalyst Bangladesh as implementing partner of its ICT market development Initiatives, primarily to support promotion and replication of Grameen Phones (GP) Community Information Center (CIC) project.<br>
    <br>
  </li>
  <li>In September 2008, BIID launched a pilot campaign entitled e-Krishok to promote information and knowledge based services of CICs to support farmers and agro-based economic activities. <br>
    <br>
  </li>
  <li> In October 2008, BIID in collaboration with Bangladesh Rural Development Academy (BARD) initiated a local e-governance project with approval of the Ministry of LGRD to pilot use of ICTs in processing and delivery of public information and services offered by Local Government. <br>
    <br> 
  </li>
  <li> In November 2008, BIID launched its own CIC and co-branded it as Batighar with a vision to develop it into an R&amp;D facility to try out new service, service delivery tools/techniques, service mix (bundling), revenue model and community engagement strategies. <br>
    <br> 
  </li>
  <li> In Jan 2009, under the initiative of BTN, BIID was appointed as the District Resource Center (DRC) for Dhaka District to develop provision for training and consulting to deliver/enhance skills, knowledge and managerial capacity development of telecenter owners/operators. <br>
    <br>
  </li>
  <li> During February to March 2009 BIID, commissioned by Consumers International (CI) and in collaboration with Consumers Association of Bangladesh (CAB) developed and launched a web based platform e-Complain for citizens of Bangladesh to voice and seek remedy in case of violation of their rights as consumers. <br>
    <br> 
  </li>
  <li> During April to May, 2009 BIID conducted assessment of existing services, business model and impact of 2 pilot Rural Technology Centers (RTC) under FoSHoL project of EU implemented by Practical Action and supervised by IRRI. <br>
    <br> 
  </li>
  <li> In May, 2009 BIID partnered with DCCI and JCI to launch a web-based portal to help farmers and homestead based micro and small entrepreneurs gain access to markets for their produces, seek information and advisory services pertaining to access to finance, production technique, access to appropriate technology and quality assurance issues. <br>
  </li>
</ul>
