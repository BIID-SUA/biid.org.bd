                  <p><strong>Discussion session on Telecentre/ICT4D</strong></p>
                  <p><img src="<?php echo $imagepath;?>/workshop.JPG"></p>
                  <p>Bangladesh Institute of ICT in Development (BIID) has organized a Knowledge 
                      sharing session on Telecentre/ICT4D practices in Bangladesh on 15th January 2011 
                      at BRAC Center Inn, Mohakhali, Dhaka. Leading organizations who are working on 
                      ICT4D like Grameen Phone, D.net, Grameen Communication, BTN, A2I/PMO, AIS, 
                      Katalyst, ARBAN IT, WIN Incorporate, BRAC Net, Dhaka Ahsania Mission and CISCO 
                      participated in this event. Dr. Roger Harris (Telecentre/ICT4D specialist) was 
                      present as the key discussant. Mr. Md.N.I.Khan (PS-1 to PM and NPD, A2I) and 
                      Md.Shahid Uddin Akbar (CEO of BIID) were also present among others. Mr. Anir 
                      Chowdhury, Policy Advisor, A2I/PMO moderated this session. Participants shared 
                      their expert experiences and knowledge on various aspects on Telecentre/ICT4D. 
                      &quot;The more a telecentre is required to generate revenues, the less emphasis it 
                      will place on supporting development and the more it will place on 
                      revenue-generating services.&quot;- Dr. Roger Harris (Telecentre/ICT4D specialist) 
                      opined during the session. Mr. Md. N.I. Khan highlighted on to provide the 
                      proper services through telecentre to make these sustain.</p>
