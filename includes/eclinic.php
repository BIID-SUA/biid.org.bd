                  <p><strong>e-Clinic : ICT for Health</strong></p>
                  <p>BIID has launched an ICT based healthcare initiatives e-Clinic on 1st October 2010. e-Clinic is an internet based telemedicine service for the rural poor of remote areas of Bangladesh where graduate doctors or quality medical service is not available.  On the occasion BIID has tested the initiatives at the two of its Batighar branded information service point in Kapasia of Gazipur district and Shoronkhola of Bagherhat district. Jhenidah Ex-Cadets Association (JEXCA) provided BIID the technical support in launching the e-Clinic pilot test. Dr. Zillur Rahman, MBBS and a Member of JEXCA provided consultation to the patients through teleconference from BIID Office. The Managers of Batighar Kapasia and Batighar Shoronkhola organized the initiative including sorting the patients and arranging the technical facilities at local level. BIID is planning to launch the service in 10 locations during the pilot phase and will scale up to 500 centres by 2012.</p>
