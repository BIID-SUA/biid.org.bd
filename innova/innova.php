                  <p><b>Entrepreneurship Development through Business Incubation:&nbsp; </b>Bangladesh 
                      Institute of ICT in Development (BIID), a private sector initiative understand 
                      that Bangladesh is a land of micro entrepreneurs who are contributing a major 
                      portion of our economy but we are missing a huge basket of the potential young 
                      entrepreneurs who can contribute a lot in terms of innovation and growth. We 
                      have a hard working work force (especially, fresh graduates) who are blessed 
                      with common sense and are capable to run any sort of business with minimum 
                      investment and support. Young entrepreneurs usually lack of enough capital, 
                      market knowledge and hands on experiences to start a new venture and face 
                      difficulties to sustain as an entrepreneur. To respond to the latent demand, 
                      BIID developed a service titled Innova as a new unit which will work as 
                      “Business Incubator” to offer incubation service for the start up businesses 
                      mainly targeting the young entrepreneurs. </p>
                  <p><b>What is a Business Incubator?&nbsp; </b>Business Incubator is a facility established 
                      to nurture young (start up) firms during their early months or years. It usually 
                      provides affordable space, shared offices and services, hand-on management 
                      training, marketing support and, often, access to some form of financing. Innova 
                      aims to introduce similar services to assist the young entrepreneurs in 
                      realizing the potential of their business faster. Business Incubator will 
                      promote creative businesses and service based initiatives.</p>
                  <p>
                      <b>Key Products & Services:&nbsp; </b>BIID will provide two categories of products and 
                      services to its clients. These are -</p>
                  <ul>
                      <li><b>First:</b> Physical infrastructure consisting primarily of office space and 
                          basic computer hardware, connectivity and equipment. BIID will also provide 
                          office supplies and stationary at cost in order to increase its clients’ 
                          convenience.</li>
                      <li><b>Second:</b> Technical assistance to its clients through linking a pool of 
                          experts who will be able to assist them to guide to ensure proper development. 
                          These will consist of, but will not be limited to, help with legal, access to 
                          finance, marketing, and market research, as well as mentoring services. This 
                          basket will also contain capacity building (through a training calendar) and 
                          market linkage support.</li>
                  </ul>
                  <p>
                      <b>Incubation Period:&nbsp; </b>On average, incubator clients will be allowed for 12-24 
                      months depending on the nature of businesses.</p>
                  <p>
                      <b>Strategic Partners: </b>Innova will ensure interaction between the Young 
                      Entrepreneurs with the leading Chambers (like Dhaka Chamber of Commerce – DCCI, 
                      Junior Chamber International - JCI and organizations like SME foundation, Banks 
                      etc.</p>
                  <p>
                      <b>How to Start?&nbsp; </b>Apply in prescribed application and submit your business 
                      plan with 2 references.</p>
